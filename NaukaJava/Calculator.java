public class Calculator {
    public int dodaj(int firstNumber, int secondNumber){
        return firstNumber+secondNumber;
    }
    public int odejmij(int firstNumber, int secondNumber){
        return firstNumber-secondNumber;
    }
    public int podziel(int firstNumber, int secondNumber){
        return firstNumber/secondNumber;
    }
    public int pomnoz(int firstNumber, int secondNumber){
        return firstNumber*secondNumber;
    }
    public int mod(int firstNumber, int secondNumber){
        return firstNumber%secondNumber;
    }
}
